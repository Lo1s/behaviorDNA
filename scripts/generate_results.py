"""
scripts/generate_results.py
===========================
Render the README "Results at a glance" section from reports/*.json.

The README and the pipeline used to drift apart (the identification headline
sat at a stale snapshot while reports/eval_metrics.json moved on). This script
makes the committed reports the single source of truth: it rewrites the block
between the RESULTS markers in README.md, and CI runs ``--check`` to fail the
build whenever the README no longer matches the reports.

Sources:
  reports/eval_metrics.json                — identification accuracy/F1 + bootstrap CIs
  reports/external_identification.json     — public-corpus scale-up + verification (Phase 6)
  reports/architecture_comparison.json     — LSTM-AE chunk AUC, synthetic cheats on real legit
  reports/architecture_comparison_cs2cd.json — LSTM-AE chunk AUC on CS2CD real cheats
  reports/architecture_comparison_real.json  — LSTM-AE chunk AUC on own recorded cheats
  reports/inference_benchmark.json         — latency / throughput / ONNX parity
  reports/same_hardware.json               — same-hardware pair (hydra vs dninix); regenerate via `python -m scripts.same_hardware_eval`

Every headline number now comes from a committed JSON report — there are no
hand-entered constants.

Usage:
    python -m scripts.generate_results            # rewrite README block
    python -m scripts.generate_results --check    # exit 1 if README is stale (CI)
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
README = ROOT / "README.md"
REPORTS = ROOT / "reports"

BEGIN = "<!-- RESULTS:BEGIN — auto-generated by scripts/generate_results.py; edit the script, not this block -->"
END = "<!-- RESULTS:END -->"


def _load(name: str) -> dict:
    with open(REPORTS / name) as f:
        return json.load(f)


def _fmt_ci(ci: list[float]) -> str:
    return f"{ci[0]:.2f}–{ci[1]:.2f}"


def _external_rows() -> str:
    """Public-corpus scale-up + verification rows (Phase 6). Empty string if
    reports/external_identification.json doesn't exist yet."""
    path = REPORTS / "external_identification.json"
    if not path.exists():
        return ""
    ext = _load("external_identification.json")
    bal = ext["balabit"]
    sap = ext["sapimouse"]
    full = next(e for e in sap["users_curve"] if e["n_users"] == sap["n_users"])
    bal_acc = bal["closed_set"]["accuracy"]
    bal_eer = bal["verification"]["session_eer"]
    n_trials = bal["verification"]["n_genuine"] + bal["verification"]["n_impostor"]
    return f"""
| **Public corpus, {bal['n_users']} users (Balabit)** — *mouse-only, same pipeline* | **{bal_acc:.2f}** acc (95% CI {_fmt_ci(bal['closed_set']['accuracy_ci95'])}, chance 0.10) · impostor-detection **EER {bal_eer:.3f}** ({n_trials} labelled sessions) |
| **Public corpus, {sap['n_users']} users (SapiMouse)** — *scale stress-test* | {full['accuracy_mean']:.2f} acc (chance {full['chance']:.3f} — {full['accuracy_mean']/full['chance']:.0f}× chance) from ~6 train windows/user; open-set ≈ chance → data-starved, the Phase-8 pretraining motivation |"""


def build_results_markdown() -> str:
    ev = _load("eval_metrics.json")
    synth = _load("architecture_comparison.json")["LSTM-AE"]["chunk_auc"]
    cs2 = _load("architecture_comparison_cs2cd.json")["LSTM-AE"]["chunk_auc"]
    real = _load("architecture_comparison_real.json")["LSTM-AE"]["chunk_auc"]
    bench = _load("inference_benchmark.json")

    if not ev.get("evaluated"):
        raise SystemExit(
            "eval_metrics.json says evaluated=false — run `dvc repro` first."
        )

    acc, acc_ci = ev["test_accuracy"], ev["test_accuracy_ci95"]
    f1, f1_ci = ev["f1_weighted"], ev["f1_weighted_ci95"]
    n_test, n_classes = ev["n_test_windows"], ev["n_classes"]

    sk_p50 = bench["single_window_latency"]["sklearn"]["p50_ms"]
    ox_p50 = bench["single_window_latency"]["onnx"]["p50_ms"]
    sk_tp = bench["throughput_windows_per_s"]["sklearn"]
    ox_tp = bench["throughput_windows_per_s"]["onnx"]
    fidelity = bench["onnx_fidelity_ok"]
    prob_mae = bench["probability_mae"]
    agreement = bench["label_agreement"]

    onnx_line = (
        f"ONNX export **bit-faithful** (probability MAE {prob_mae:.0e}, "
        f"{agreement:.0%} label agreement — regression-gated in CI) · "
        f"ONNX p50 **{ox_p50:.3f} ms**/window (~{ox_tp/1000:.0f}k windows/s) · "
        f"sklearn p50 {sk_p50:.2f} ms (~{sk_tp/1000:.0f}k windows/s)"
        if fidelity
        else (
            f"⚠️ ONNX export currently UNFAITHFUL (probability MAE {prob_mae:.2e}) — "
            f"sklearn is the production path (p50 {sk_p50:.2f} ms, ~{sk_tp/1000:.0f}k windows/s)"
        )
    )

    sh = _load("same_hardware.json")

    return f"""{BEGIN}

**Player identification** (behavioural biometric, real GTA, per 30 s window — 95% CIs from a {ev['n_bootstrap']}-resample window bootstrap; intervals are wide because the test set is {n_test} windows):

| Setting | Result |
|---|---|
| {n_classes} players (real GTA) | **{acc:.2f}** acc (95% CI {_fmt_ci(acc_ci)}) · {f1:.2f} F1 (95% CI {_fmt_ci(f1_ci)}) |
| same-hardware pair *(no hardware confound)* | **{sh['accuracy']:.2f}** acc ({sh['majority_baseline']:.2f} baseline, 95% CI {_fmt_ci(sh['accuracy_ci95'])}; {sh['n_test_windows']} test windows) — the honest biometric ([notebook 12](notebooks/12_explainability.ipynb)) |{_external_rows()}

**Cheat detection** — chunk-level ROC AUC of the LSTM autoencoder, three independent settings (hand-crafted window features ≈ 0.50 = chance for aimbot):

| Setting | Result |
|---|---|
| Synthetic cheats on real legit — *approach proof* | aimbot **{synth['aimbot']:.2f}** · triggerbot **{synth['triggerbot']:.2f}** · macro {synth['macro']:.2f} |
| **External game (CS2CD, 10 players, real cheats)** — *generalisation* | **{cs2:.2f}** |
| Own *recorded* real cheats (1 player) — *hardest, most honest* | aimbot {real['aimbot']:.2f} · triggerbot {real['triggerbot']:.2f} · macro {real['macro']:.2f} |

LSTM-AE vs TCN-AE vs Transformer-AE are **statistically tied in every setting** → capacity isn't the bottleneck, data is ([ARCHITECTURE_COMPARISON.md](docs/ARCHITECTURE_COMPARISON.md)).

**Engineering:** {onnx_line} · mock→real **drift 20/25 features** significant (KS+PSI) · MLflow registry + CI gates.

*Regenerated from `reports/*.json` by `python -m scripts.generate_results` — CI fails if this block is stale.*

{END}"""


def render_readme(readme_text: str) -> str:
    start = readme_text.find(BEGIN)
    end = readme_text.find(END)
    if start == -1 or end == -1:
        raise SystemExit(
            f"README.md is missing the results markers ({BEGIN[:30]}… / {END})."
        )
    return (
        readme_text[:start] + build_results_markdown() + readme_text[end + len(END) :]
    )


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--check",
        action="store_true",
        help="exit 1 if README.md is out of date instead of rewriting it",
    )
    args = parser.parse_args(argv)

    current = README.read_text()
    rendered = render_readme(current)

    if args.check:
        if rendered != current:
            print(
                "README.md results block is stale — run "
                "`python -m scripts.generate_results` and commit.",
                file=sys.stderr,
            )
            return 1
        print("README.md results block is up to date.")
        return 0

    README.write_text(rendered)
    print("README.md results block regenerated.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
